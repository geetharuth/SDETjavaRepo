package day3;

public class Decrementoperator {

	public static void main(String[] args) {
		// case 1
		int a=10;
		a--; //a=a-1;
		System.out.println(a);
		
		//case 2--post increment 
		int b=100;
		
		int res=b--;
		System.out.println(res);
		System.out.println(b);
		
		//case3--pre decrement
		int a1=100;
		int res1=--a1;
		System.out.println(a1);
		System.out.println(res1);
		
		
		
		
		
		
		
		
	}

}
